function checkName(name) {
    if (typeof name !== 'string') {
        return 'invalid'
    } else {
        const lastChar = ['a','y', 'i','e','o','u','w'];
        const nameLastChar = name[name.length - 1].toLowerCase();
        if (lastChar.includes(nameLastChar)) {
            return 'Good Name';
        }
        else {
            return 'bad Name';
        }
    }
}

