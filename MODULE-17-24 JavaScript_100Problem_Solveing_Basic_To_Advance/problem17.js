/*
Find all the odd numbers from 61 to 100. use while loop
 */
console.log('Find all the odd numbers from 61 to 100. use while loop');
let a = 61;
while (a <= 100) {
    if (a % 2 == 1) {
        console.log('the odd numbers from 61 to 100 :', a );
    }
    a++
}


