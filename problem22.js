/*
Find all the odd numbers from 61 to 100 used for loop.
 */
console.log('Find all the odd numbers from 61 to 100 used for loop')
for (a = 61; a <= 100; a++){
    if (a % 2 == 1) {
        console.log('All the odd numbers from 61 to 100 : ', a);
    }
}


